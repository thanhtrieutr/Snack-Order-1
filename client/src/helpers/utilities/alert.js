import { toast } from 'react-toastify';

export function successAlert(string) {
    toast.success(string, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        });
}

